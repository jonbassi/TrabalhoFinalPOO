    //Botão excluir pressionado
    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
         
        try {
            
            //Verifica qual linha está selecionado e qual o código do funcionário
            //da linha que está selecionada
            int linhaSelecionada = tabelaProduto.getSelectedRow();
            String codigo  = tabelaProduto.getValueAt(linhaSelecionada, 0).toString();
            
            //Pergunta se o usuário tem certeza que deseja excluir
            int dialogButton = 0;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir?", 
                    "ATENÇÃO!", dialogButton);
            
            //Se o usuário confirma a exclusão, recupera o objeto produto
            //através do código e envia o pedido de exclusão
            if (dialogResult == JOptionPane.YES_OPTION) {
            
            Produto produto = getProd.selecionarProduto(Integer.parseInt(codigo));
            getProd.removerProduto(produto);
            
            //Exibe mensagem de empregado excluído com sucesso
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Produto excluído com sucesso!");
            
            //Fecha a tela e abre novamente para forçar a atualização da tabela
            GerenciarEstoqueGUI tela = new GerenciarEstoqueGUI();
            tela.setVisible(true);
            dispose();
        }
            
        //Mensagem de erro é exibida caso nenhuma linha tenha sido selecionada                
        } catch (Exception e) {
            JOptionPane optionPane = new JOptionPane("Selecione um registro para excluir!", 
            JOptionPane.ERROR_MESSAGE);    
            JDialog dialog = optionPane.createDialog("Erro");
            dialog.setAlwaysOnTop(true);
            dialog.setVisible(true);
            
        }
    }//GEN-LAST:event_buttonExcluirActionPerformed

